export function getTodaySantoral(): { dateString: string; data: SantoralDay | undefined } {
  // Use current local time in Chile (approx) or user local time
  const today = new Date();
  const month = String(today.getMonth() + 1).padStart(2, "0");
  const day = String(today.getDate()).padStart(2, "0");
  const dateString = `${month}-${day}`;

  return {
    dateString,
    data: santoralData[dateString]
  };
}
